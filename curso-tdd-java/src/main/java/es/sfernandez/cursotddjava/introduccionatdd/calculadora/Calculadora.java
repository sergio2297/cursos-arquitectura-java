package es.sfernandez.cursotddjava.introduccionatdd.calculadora;

public class Calculadora {

	//---- Constructor ----
	public Calculadora() {}
	
	//---- Metodos ----
	public int sumar(final int num1, final int num2) {
		return num1 + num2;
	}

	public int restar(int num1, int num2) {
		return num1 - num2;
	}

	public int multiplicar(final int num1, final int num2) {
		return num1 * num2;
	}

}
